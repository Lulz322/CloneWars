#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	unsigned int len2;

	if (*needle == '\0' || !needle)
		return ((char *)haystack);
	len2 = ft_strlen(needle);
	while (*haystack && len-- >= len2)
	{
		if (*haystack == *needle && ft_memcmp(haystack, needle, len2) == 0)
			return ((char *)haystack);
		haystack++;
	}
	return (NULL);
}
